/**
 * Batch sheet renderer — assembles run outputs into reviewable HTML surfaces.
 *
 * Produces an HTML file with a CSS grid layout that references output images.
 * Zero dependencies — works in any browser. Slot labels, seeds, and metadata
 * are embedded for human review.
 *
 * Future: add PNG rendering via sharp if needed.
 */

import { writeFile } from 'node:fs/promises';
import { join, relative } from 'node:path';
import { existsSync } from 'node:fs';
import { getRunsDir } from './paths.js';

/**
 * Resolve the image path for a slot result.
 * Returns a relative path from the sheet directory to the output image.
 */
function resolveImagePath(projectRoot, result, batchDir) {
  if (!result.selected_output || !result.run_id) return null;

  const imagePath = join(getRunsDir(projectRoot), result.run_id, 'outputs', result.selected_output);
  if (!existsSync(imagePath)) return null;

  // Relative from sheets/ dir to the image
  const sheetsDir = join(batchDir, 'sheets');
  return relative(sheetsDir, imagePath).replace(/\\/g, '/');
}

/**
 * Render a batch sheet as HTML.
 *
 * @param {Object} opts
 * @param {Object} opts.manifest — batch manifest
 * @param {Object} opts.mode — batch mode definition
 * @param {string} opts.projectRoot
 * @param {string} opts.batchDir
 * @returns {string} HTML content
 */
export function renderSheetHTML({ manifest, mode, projectRoot, batchDir }) {
  const assembly = mode.assembly || {};
  const layout = assembly.layout || 'grid';
  const columns = assembly.columns || computeColumns(manifest.slots.length);
  const showSlotLabels = assembly.annotate_slots !== false;
  const showSeed = assembly.show_seed || false;

  const title = `${manifest.mode_id} — ${manifest.subject_id || manifest.theme || manifest.project_id}`;
  const slots = manifest.slots || [];

  // Build slot cells
  const cells = slots.map(slot => {
    const imgPath = resolveImagePath(projectRoot, slot, batchDir);
    const hasImage = !!imgPath;

    return {
      slot_id: slot.slot_id,
      label: slot.label,
      run_id: slot.run_id,
      selected_output: slot.selected_output,
      imgPath,
      hasImage,
      seed: findSlotSeed(manifest, slot),
    };
  });

  // Generate HTML
  const css = generateCSS(layout, columns, cells.length);
  const grid = cells.map((cell, i) => renderCell(cell, i, showSlotLabels, showSeed)).join('\n');

  return `<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="utf-8">
<title>${escapeHtml(title)}</title>
<style>${css}</style>
</head>
<body>
<header>
  <h1>${escapeHtml(title)}</h1>
  <p class="meta">
    Batch: ${escapeHtml(manifest.batch_id)} |
    Mode: ${escapeHtml(manifest.mode_id)} |
    Type: ${escapeHtml(mode.batch_type)} |
    ${slots.length} slots |
    ${manifest.created_at?.slice(0, 16) || '?'}
  </p>
</header>
<div class="grid">${grid}
</div>
<footer>
  <p>Generated by style-dataset-lab — ${manifest.created_at || ''}</p>
</footer>
</body>
</html>`;
}

/**
 * Compute optimal column count based on slot count.
 */
function computeColumns(slotCount) {
  if (slotCount <= 3) return slotCount;
  if (slotCount <= 4) return 2;
  if (slotCount <= 6) return 3;
  if (slotCount <= 9) return 3;
  return 4;
}

