function findSlotSeed(manifest, slot) {
  // Not always available — return null if not found
  return null; // Could be enriched from run manifests later
}

/**
 * Escape HTML entities.
 */
function escapeHtml(str) {
  if (!str) return '';
  return String(str)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}

/**
 * Save the sheet HTML to the batch sheets directory.
 */
export async function saveSheet(batchDir, filename, html) {
  const sheetsDir = join(batchDir, 'sheets');
  const sheetPath = join(sheetsDir, filename);
  await writeFile(sheetPath, html);
  return sheetPath;
}
